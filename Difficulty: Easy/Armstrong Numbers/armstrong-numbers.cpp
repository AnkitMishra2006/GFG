class Solution {
  public:
    bool armstrongNumber(int n) {
        // code here

        int arm = 0;
        int x = n;
        while (x > 0) {
            int ldig = x % 10;
            x = x / 10;
            arm += ldig * ldig * ldig;
        }
        return (arm == n);
    }
};

